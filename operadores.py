# ==============================
# 1. Operadores de comparación
# ==============================

# Ejemplos de diferentes operadores de comparación
A = 5
B = 10

print("\nEjemplos de operadores de comparación:")
print(f"A = {A}, B = {B}")
print("A < B:", A < B)  # Menor que
print("A > B:", A > B)  # Mayor que
print("A <= B:", A <= B)  # Menor o igual que
print("A >= B:", A >= B)  # Mayor o igual que
print("A == B:", A == B)  # Igual a
print("A != B:", A != B)  # Diferente de

# ==============================
# 2. Operadores lógicos
# ==============================
X = True
Y = False

print("\nEjemplos de operadores lógicos:")
print("X =", X, ", Y =", Y)
print("X and Y:", X and Y)  # AND lógico
print("X or Y:", X or Y)  # OR lógico
print("not X:", not X)  # NOT lógico

# ==============================
# 3. Operadores aritméticos
# ==============================
C = 15
D = 4

print("\nEjemplos de operadores aritméticos:")
print("C =", C, ", D =", D)
print("Suma (C + D):", C + D)
print("Resta (C - D):", C - D)
print("Multiplicación (C * D):", C * D)
print("División (C / D):", C / D)
print("División entera (C // D):", C // D)
print("Módulo (C % D):", C % D)
print("Exponente (C ** D):", C**D)
